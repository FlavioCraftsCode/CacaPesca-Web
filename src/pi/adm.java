// Método auxiliar para organizar a interface
private void atualizarTabela() {
    try {
        PedidoDAO dao = new PedidoDAO();
        List<Pedido> lista = dao.listarTodos();

        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setRowCount(0); 
        
        // Configura as colunas
        model.setColumnIdentifiers(new Object[]{"ID", "Cliente", "Produto", "Quantidade", "Valor (R$)"});

        for (Pedido p : lista) {
            model.addRow(new Object[]{
                p.getId(),
                p.getCliente(),
                p.getProduto(),
                p.getQuantidade(),
                "R$ " + p.getValor().setScale(2, RoundingMode.HALF_UP)
            });
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro ao atualizar tabela: " + e.getMessage());
    }
    }//GEN-LAST:event_tableAncestorAdded

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
    // 1. Verifica se há uma linha selecionada
    int selectedRow = table.getSelectedRow();

    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(null, "Por favor, selecione um item para excluir.");
        return;
    }

    // 2. Obtém o ID (geralmente na coluna 0)
    int id = (int) table.getValueAt(selectedRow, 0);

    // 3. Pede confirmação ao usuário
    int confirm = JOptionPane.showConfirmDialog(null, 
            "Tem certeza que deseja excluir este registro?", 
            "Confirmação", JOptionPane.YES_NO_OPTION);

    if (confirm == JOptionPane.YES_OPTION) {
        // 4. Chama o DAO para realizar a exclusão
        PedidoDAO dao = new PedidoDAO();
        
        if (dao.excluir(id)) {
            JOptionPane.showMessageDialog(null, "Dados excluídos com sucesso!");
            
            // 5. Atualiza a interface (você pode remover a linha ou atualizar a tabela toda)
            atualizarTabela(); 
        } else {
            JOptionPane.showMessageDialog(null, "Erro ao excluir dados no banco.");
        }
    }
    }//GEN-LAST:event_btndeleteActionPerformed

    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
    // 1. Obtém o texto digitado
    String termo = txtsearch.getText().trim();

    // 2. Limpa a tabela antes de mostrar os novos resultados
    DefaultTableModel model = (DefaultTableModel) table.getModel();
    model.setRowCount(0);

    try {
        // 3. Chama o DAO para buscar os dados filtrados
        PedidoDAO dao = new PedidoDAO();
        java.util.List<Pedido> resultados = dao.buscar(termo);

        // 4. Preenche a tabela com os resultados do banco
        for (Pedido p : resultados) {
            model.addRow(new Object[]{
                p.getId(),
                p.getCliente(),
                p.getProduto(),
                p.getQuantidade(),
                "R$ " + p.getValor().setScale(2, java.math.RoundingMode.HALF_UP)
            });
        }
        
        // 5. Configura o sorter para permitir ordenação manual (opcional)
        table.setRowSorter(new javax.swing.table.TableRowSorter<>(model));

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro ao realizar a busca: " + e.getMessage());
    }
    }//GEN-LAST:event_btnsearchActionPerformed

   
