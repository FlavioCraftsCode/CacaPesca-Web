    private void btncreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncreateActionPerformed
try {
        // 1. Captura e Conversão (Interface -> Objeto)
        Pedido novoPedido = new Pedido(
            txtcliente.getText(),
            txtproduto.getText(),
            Integer.parseInt(txtquantidade.getText()),
            new BigDecimal(txtvalor.getText())
        );

        // 2. Uso do DAO para salvar
        PedidoDAO dao = new PedidoDAO();
        if (dao.cadastrar(novoPedido)) {
            JOptionPane.showMessageDialog(null, "Dados cadastrados com sucesso!");
            atualizarTabela(); // Recarrega a JTable
            limparCampos();
        } else {
            JOptionPane.showMessageDialog(null, "Falha ao cadastrar.");
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Dados inválidos: " + e.getMessage());
    }
    
    }//GEN-LAST:event_btncreateActionPerformed

